public class Character {
    String nome;
    int velocidade;
    int forca;
    int vida, defesa, destreza;

    public Character(String nome, int velocidade, int forca, int vida, int defesa, int destreza) {
        this.nome = nome;
        this.velocidade = velocidade;
        this.forca = forca;
        this.vida = vida;
        this.defesa = defesa;
        this.destreza = destreza;
    }
}
